// ============================================================
// Framebuffer — 128 columns x 8 pages (64px total height)
// ============================================================
static uint8_t s_fb[OLED_PAGES][OLED_WIDTH];

// ============================================================
// I2C handle
// ============================================================
static i2c_master_bus_handle_t  s_bus_handle  = NULL;
static i2c_master_dev_handle_t  s_dev_handle  = NULL;

// ============================================================
// Low-level I2C helpers
// ============================================================

static esp_err_t oled_send_cmd(uint8_t cmd)
{
    uint8_t buf[2] = { 0x00, cmd };  // 0x00 = control byte (command)
    return i2c_master_transmit(s_dev_handle, buf, 2, 100);
}

static esp_err_t oled_send_data(const uint8_t *data, size_t len)
{
    // Prepend 0x40 control byte (data mode)
    uint8_t *buf = malloc(len + 1);
    if (!buf) return ESP_ERR_NO_MEM;
    buf[0] = 0x40;
    memcpy(buf + 1, data, len);
    esp_err_t ret = i2c_master_transmit(s_dev_handle, buf, len + 1, 100);
    free(buf);
    return ret;
}

// ============================================================
// SSD1306 initialisation sequence
// ============================================================

static const uint8_t s_init_cmds[] = {
    0xAE,        // display off
    0xD5, 0x80,  // set display clock divide ratio
    0xA8, 0x3F,  // set multiplex ratio (63 = 64 rows)
    0xD3, 0x00,  // set display offset
    0x40,        // set start line to 0
    0x8D, 0x14,  // enable charge pump
    0x20, 0x00,  // set memory addressing mode: horizontal
    0xA1,        // segment remap (mirror horizontally)
    0xC8,        // COM output scan direction (flip vertically)
    0xDA, 0x12,  // set COM pins hardware config
    0x81, 0xCF,  // set contrast
    0xD9, 0xF1,  // set pre-charge period
    0xDB, 0x40,  // set VCOMH deselect level
    0xA4,        // output follows RAM content
    0xA6,        // normal display (not inverted)
    0xAF,        // display on
};

// ============================================================
// Public API
// ============================================================

esp_err_t oled_init(void)
{
    // --- I2C bus init ---
    i2c_master_bus_config_t bus_cfg = {
        .i2c_port      = I2C_NUM_0,
        .sda_io_num    = OLED_SDA_PIN,
        .scl_io_num    = OLED_SCL_PIN,
        .clk_source    = I2C_CLK_SRC_DEFAULT,
        .glitch_ignore_cnt = 7,
        .flags.enable_internal_pullup = true,
    };
    ESP_ERROR_CHECK(i2c_new_master_bus(&bus_cfg, &s_bus_handle));

    // --- Device registration ---
    i2c_device_config_t dev_cfg = {
        .dev_addr_length = I2C_ADDR_BIT_LEN_7,
        .device_address  = OLED_I2C_ADDR,
        .scl_speed_hz    = 400000,  // 400kHz fast mode
    };
    ESP_ERROR_CHECK(i2c_master_bus_add_device(s_bus_handle, &dev_cfg, &s_dev_handle));

    // --- SSD1306 init sequence ---
    for (size_t i = 0; i < sizeof(s_init_cmds); i++) {
        esp_err_t ret = oled_send_cmd(s_init_cmds[i]);
        if (ret != ESP_OK) {
            ESP_LOGE(TAG, "init cmd 0x%02x failed: %s",
                     s_init_cmds[i], esp_err_to_name(ret));
            return ret;
        }
    }

    memset(s_fb, 0, sizeof(s_fb));
    oled_flush();

    ESP_LOGI(TAG, "SSD1306 initialised");
    return ESP_OK;
}

void oled_clear(void)
{
    memset(s_fb, 0, sizeof(s_fb));
    oled_flush();
}

void oled_write_string(uint8_t page, uint8_t col, const char *str)
{
    while (*str && col < OLED_WIDTH) {
        uint8_t c = (uint8_t)*str;
        if (c < 32 || c > 126) c = 32;  // replace non-printable with space

        const uint8_t *glyph = font5x7[c - 32];
        for (int i = 0; i < 5 && col < OLED_WIDTH; i++, col++) {
            s_fb[page][col] = glyph[i];
        }
        // 1px gap between characters
        if (col < OLED_WIDTH) s_fb[page][col++] = 0x00;

        str++;
    }
}

void oled_flush(void)
{
    for (uint8_t page = 0; page < OLED_PAGES; page++) {
        oled_send_cmd(0xB0 + page);  // set page address
        oled_send_cmd(0x00);         // set column low nibble
        oled_send_cmd(0x10);         // set column high nibble
        oled_send_data(s_fb[page], OLED_WIDTH);
    }
}